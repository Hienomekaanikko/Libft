#include "libft.h"
/*
Check for alphabet. Returns true or false.
*/
int	ft_isalpha(int c)
{
	if ((c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z'))
	{
		return (0);
	}
	return (1);
}
